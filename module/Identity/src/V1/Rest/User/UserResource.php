    {
        $this->userService = $userService;
    }

    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {
        try{

            $responce = $this->userService->registerUser($data->emailAddress, $data->password);

            return $responce;
        }catch (\RuntimeException $e){
            if($e->getCode() == UserService::ERROR_USER_EXIST_CODE){
                return new ApiProblem(422, UserService::ERROR_USER_EXIST_MSG);
            }

            return new ApiProblem(500, $e->getMessage());
        }
    }

