    {
        try{
            $this->userService->resetPassword($data->email, $data->token, $data->newPassword);
        }catch (\RuntimeException $e){
            if($e->getCode() == UserService::ERROR_INVALD_RESET_TOKEN_CODE){
                return new ApiProblem(404, $e->getMessage());
            }

            return new ApiProblem(500, $e->getMessage());
